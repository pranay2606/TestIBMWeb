package com.java.pack1;

public class SwitchCaseEx {
	public static void main(String[] args) {
		String fruit="mango";
		
		switch(fruit) {
		case "mango":
			System.out.println("Yellow Color");
			break;
		case "apple":
			System.out.println("Red Color");
			break;
		case "orange":
			System.out.println("Orange Color");
			break;
		}
	}
}
